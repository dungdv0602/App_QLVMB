package com.example.da1_group6.model;

public class HangMB {
    String mamb, tenmb;

    public HangMB(String mamb, String tenmb) {
        this.mamb = mamb;
        this.tenmb = tenmb;
    }

    public HangMB() {
    }

    public String getMamb() {
        return mamb;
    }

    public void setMamb(String mamb) {
        this.mamb = mamb;
    }

    public String getTenmb() {
        return tenmb;
    }

    public void setTenmb(String tenmb) {
        this.tenmb = tenmb;
    }
}
